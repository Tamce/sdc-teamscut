// 翻译组件
window.zh = false;
window.tr = function(el) {
  var en = $(el).attr("data-tr");
  var key = $(el).attr("data-tr-key");
  if (!key) {
    key = en;
  }
  if (window.zh) {
    if (!!$(el).attr("data-tr-zh")) {
      $(el).text($(el).attr("data-tr-zh"));
    } else {
      $(el).text(window.dict[key]);
    }
    return;
  }
  $(el).text(en);
};
window.toggleLocale = function(nochange) {
  if (!nochange) {
    window.zh = !window.zh;
  }
  if (window.zh) {
    $("body").removeClass("en");
    $("body").addClass("zh");
  } else {
    $("body").removeClass("zh");
    $("body").addClass("en");
  }
  $("[data-tr]").map(function(id, el) {
    tr(el);
  });
  $($("iframe").contents()).find("[data-tr]").map(function(id, el) {
    tr(el);
  });
  if (typeof window.localeChanged == "function") {
    window.localeChanged();
  }
};
window.reloadLocaleScope = function(scope) {
  $("[data-tr]", scope).map(function(id, el) {
    if (!!$(el).attr("data-tr-inited")) return;
    if (!!$(el).attr("data-tr-key")) {
      return;
    }

    if ($(el).attr("data-tr") == "") {
      $(el).attr("data-tr", $(el).text());
    } else {
      $(el).text($(el).attr("data-tr"));
    }

    $(el).attr("data-tr-inited", "true");
  });
  $("[data-tr-key],[data-tr-zh]", scope).map(function(i, el) {
    if (!!$(el).attr("data-tr-inited")) return;
    $(el).attr("data-tr", $(el).text());
    $(el).attr("data-tr-inited", "true");
  });
  toggleLocale(true);
};
window.reloadLocale = function () {
  reloadLocaleScope(document);
  reloadLocaleScope($("iframe").contents());
}

// 前端路由加载器
function load(page) {
  if (!!page == "") page = "home";
  $("#app").load("./components/" + page + ".html", null, function() {
    reloadLocale();
    $("iframe").on('load', reloadLocale);
    // 加载完毕，如果导航栏处于打开状态，收起之
    var expanded = Number($(".tmc-navbar").attr("data-expanded"));
    if (expanded == 0) return;
    $(".tmc-navbar").animate({
      right: expanded == 0 ? "0px" : "-85vw"
    });
    $(".tmc-navbar").attr("data-expanded", 1 - expanded);
  });
}
window.onhashchange = function(e) {
  if (window.location.hash == "") return;
  var hash = window.location.hash.substr(1);
  // 用于给各个单独的组件取消注册一些定时器之类的
  if (typeof window.beforeLoad == "function") {
    window.beforeLoad();
    window.beforeLoad = undefined;
  }
  load(hash);
};

